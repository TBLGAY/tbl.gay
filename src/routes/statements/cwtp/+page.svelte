  <h1 style="margin-bottom: 0.2em">
    <strong>Statement on CWTP’s closure</strong>
  </h1>
  <p style="text-align:center; margin-top: 0em; margin-bottom:2em;">
    <em>August 22, 2023</em> <br /><br />
    <i
      ><a href="/pdfs/cwtp.pdf" target="_blank" rel="noopener noreferrer"
        >This document is also available in PDF form.</a
      ></i
    >
  </p>
  <p>
    <strong>TBLGAY’s stance</strong>
  </p>
  <p>
    As you may already know, the Centre for Women and Trans People at York
    University closed its doors on August 18th, 2023. We highly recommend you
    read <a href="https://drive.google.com/file/d/1sWOeE9xyZ0GLGhWLg9pNoeCGWFNwfeP-/view" target="_blank" rel="noopener noreferrer">the Centre’s official statement on the matter</a> before this one.
  </p>
  <p>
    The news has not been easy on us — both as frequent organizational partners
    to the Centre, and also as members of the same community on campus; a
    community that both needs and deserves more services, not less.
  </p>
  <p>
    However, it is with this same conviction that we support the actions taken
    by CWTP’s Board of Directors in closing the organization. While our
    community desperately needs services, they <em>must</em> be equitable, sustainable,
    cooperative and safe in nature. It is clear that, as it stood, the Centre was
    structurally unable to fulfill these needs, despite the Board’s every effort.
  </p>
  <p>
    Thus, while we grieve the loss of a cornerstone of the community, we also
    strongly believe that this loss is better than the continuation of a
    dysfunctional and harmful structure, hard as it is to face.
  </p>
  <p>
    <strong>What we’re doing in the wake</strong>
  </p>
  <p>
    As a Community Service Group, or CSG, TBLGAY’s mission is to provide
    services to the queer community on campus. In the wake of the loss of CWTP’s
    own services to our community, we’re redoubling our efforts to live up to
    that mission.
  </p>
  <p>
    Sadly, we cannot provide services on the same scale as the Centre could’ve.
    We’re a smaller organization, with less personnel and an order of magnitude
    smaller budget. But what we can do — be a safe harbour to our community — we
    will. No matter what. And despite our limits, we’ll try our best to
    coordinate with our ally organizations to not let services like food and
    financial aid fall through the cracks.
  </p>
  <p style="text-align: center; margin-top: 1em; margin-bottom: 2em;">
    <strong>We keep us safe.</strong>
  </p>
  <p>
    Kore M.
    <br /><i />Coordinator of TBLGAY.
  </p>

